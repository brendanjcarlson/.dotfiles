#!/usr/bin/env bash

########################################################################################################################
# NVIM
########################################################################################################################
NVIM_SRC="~/.dotfiles/nvim/"
NVIM_DEST="~/.config/nvim"
########################################################################################################################

if [ ! -d $NVIM_SRC ]; then
    echo "nvim config not found at $NVIM_SRC"
    exit 1
fi

if [ -d $NVIM_DEST ]; then
    echo "Removing existing nvim config ..."
    rm -rf $NVIM_DEST
fi

echo "Linking nvim config ..."

ln -s $NVIM_SRC $NVIM_DEST

if [ $? -eq 0 ]; then
    echo "Successfully linked nvim config!"
else
    echo "Failed to link nvim config!"
    exit 1
fi

echo

########################################################################################################################
# TMUX
########################################################################################################################
TMUX_SRC="~/.dotfiles/tmux/.tmux.conf"
TMUX_DEST="~/.tmux.conf"
########################################################################################################################

if [ ! -f $TMUX_SRC ]; then
    echo "tmux config not found at $TMUX_SRC"
    exit 1
fi

if [ -f $TMUX_DEST ]; then
    echo "Removing existing tmux config ..."
    rm -rf $TMUX_DEST
fi

echo "Linking tmux config ..."

ln -s $TMUX_SRC $TMUX_DEST

if [ $? -eq 0 ]; then
    echo "Successfully linked tmux config!"
else
    echo "Failed to link tmux config!"
    exit 1
fi

echo

########################################################################################################################
# OHMYZSH
########################################################################################################################
########################################################################################################################

########################################################################################################################
# GIT
########################################################################################################################
########################################################################################################################

########################################################################################################################
# BINARIES & SCRIPTS
########################################################################################################################
BIN_SRC="~/.dotfiles/bin"
BIN_DEST="~/.local/bin"
########################################################################################################################

if [ ! -d $BIN_SRC ]; then
    echo "binaries/scripts not found at $BIN_SRC"
    exit 1
fi

if [ -d $BIN_DEST ]; then
    echo "Removing existing binaries/scripts ..."
    rm -rf $BIN_DEST
fi

echo "Linking binaries/scripts ..."

ln -s $BIN_SRC $BIN_DEST

if [ $? -eq 0 ]; then
    echo "Successfully linked binaries/scripts!"
else
    echo "Failed to link binaries/scripts!"
    exit 1
fi

echo
